package dk.cphbusiness.choir.contract.dto;

import java.io.Serializable;

public class VoiceSummary implements Serializable
{

    private int code;
    private String name;

    public VoiceSummary(int code, String name)
    {
        this.code = code;
        this.name = name;
    }

    public int getCode()
    {
        return code;
    }

    public String getName()
    {
        return name;
    }
}
